//Arithmetic Operator

let x = 5;

let y = 2;

let sum = x + y;         // sum is 7

let difference = x - y;  // difference is 3

let product = x * y;     // product is 10

let quotient = x / y;    // quotient is 2.5

let remainder = x % y;   // remainder is 1

x++;                     // x is now 6

y--;                     // y is now 1